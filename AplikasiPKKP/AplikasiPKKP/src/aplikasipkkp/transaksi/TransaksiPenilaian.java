    public String rangking(int s) {
        String rangking = "";
        
        if (s >= 98) {
            rangking = "1";
        } else if (s < 98 && s >= 95) {
            rangking = "2";
        } else if (s < 95 && s >= 92) {
            rangking = "3";
        } else if (s < 92 && s >= 89) {
            rangking = "4";
        } else if (s < 89 && s >= 86) {
            rangking = "5";
        } else if (s < 86 && s >= 83) {
            rangking = "6";
        } else if (s < 83 && s >= 80) {
            rangking = "7";
        } else if (s < 80 && s >= 77) {
            rangking = "8";
        } else if (s < 77 && s >= 74) {
            rangking = "9";
        } else if (s < 74 && s >= 70) {
            rangking = "10";
        } else {
            rangking = "Tidak Rangking";
        }
        
        return rangking;
    }
    
    public String seleksi(int nilai){
        String hasil = "";
        if (nilai <= 50){
            hasil = "Tidak Lolos";
        } else {
            hasil = "Lolos";
        }
        
        return hasil;
    }
    

