    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        String nama = "";
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select nama from peserta";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while(res.next()){
                if(txtNama.getText().equals(res.getString("nama"))){
                    nama = txtNama.getText();
                } 
            }
            if(txtNama.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Nama tidak terdapat pada data..", "gagal", JOptionPane.ERROR_MESSAGE);
            } else if(txtNama.getText().equals(nama)) {
                String r = rangking(Integer.parseInt(txtBobot.getText()));
                String l = seleksi(Integer.parseInt(txtBobot.getText()));
                PreparedStatement statement = conn.prepareStatement("insert into penilaian(jenis_penilaian, nama, seleksi, bobot, rangking) values(?,?,?,?,?)");
                statement.setString(1, cmbJenisPenilaian.getSelectedItem().toString());
                statement.setString(2, txtNama.getText());
                statement.setString(3, l);
                statement.setString(4, txtBobot.getText());
                statement.setString(5, r);
                statement.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Nama tidak terdapat pada data..", "gagal", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
        
        
        
    }//GEN-LAST:event_btnBayarActionPerformed


